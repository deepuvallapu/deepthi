package day4;

public class VariableExamples {
	
	public static void main(String[] args) {
		
		int myVar;
		
		myVar = 10;
		
		int num2 = 45;
		
		System.out.println("Sum : "+ (myVar + num2));
		System.out.println("Subs : "+ (myVar - num2));
		
		System.out.println("Rem : "+ (num2 % myVar));
		
		long longVar = 2348324748324l;
		
		float myFloatVar = 8932.483902f;
		
		System.out.println("My Var value : "+ myVar);
		
		boolean var = true;
		
		String myStringVar = "My name is saurabh";
		
		System.out.println(myStringVar);
	}

}
